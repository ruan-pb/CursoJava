package MembrosEstaticos;

public class ConversoDeDolar {
    
    
    
    
    public static double ValorAPagar (double cot, double din){
        
        double valor = (6*cot*din)/100;
        
        
        
        return cot*din+valor;
    }
        
        
        
    
}
